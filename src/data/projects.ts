export type Project = {
  slug: string
  title: string
  category: string
  area: string
  location: string
  image: string
  /** Photo gallery (big view + thumbnails). Falls back to [image]. */
  gallery?: string[]
  /** Map position [lat, lng] for the projects map. */
  coords?: [number, number]
  status: 'active' | 'sold'
  /** Project family for catalog filtering. Defaults to 'residential'. */
  kind?: 'residential' | 'business'
  /** Business-center cards have no detail page — only an "Оставить заявку" CTA. */
  cardOnly?: boolean
  /** Active project not yet on sale — shows a "Скоро в продаже" badge. */
  comingSoon?: boolean
  details?: ProjectDetails
  /** Detail-page link override (e.g. completed projects → /completed/<slug>). */
  href?: string
}

